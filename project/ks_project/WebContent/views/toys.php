		<p>Every rat needs a fort for their free-range area! You can construct
			a fort out of old cardboard boxes, tubbing, pvc pipe, thick rope for
			climbing, bird ladders, or anything else interesting that might be
			fun (but safe) for rats to play on. Remember to cut holes for windows
			and doors!</p>
