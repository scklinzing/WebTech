		<h2>
			<a id="diggingBox"></a>The Digging Box
		</h2>
		<p>
			<a
				href="https://lh4.googleusercontent.com/-iYGCBVEpFKs/VIkYwo54nyI/AA
		AAAAAAAPc/-MEO-JKt9p8/w438-h288-no/diggingbox.jpg"><img
				src="https://lh
		4.googleusercontent.com/-iYGCBVEpFKs/VIkYwo54nyI/AAAAAAAAAPc/-MEO-JKt9p8/w438-h
		288-no/diggingbox.jpg"
				alt="digging box" title="Digging Box"></a>
		</p>
		<p>This can be created several ways. If using real plants, select a
			wide bin with drain holes punctured in the bottom. Fill it halfway
			with clean soil (make sure it has no added fertilizers, chemicals or
			bugs). Add a handful of birdseed, and water lightly. Wait a few days
			until the seeds sprout. If you wait until the grass is slightly
			root-bound, the rats will have a tougher time flinging it all over
			the place. Make sure not to water the grass two days before, so it is
			not muddy when the rats are playing in it. You can also add some
			treats to get the rats interested. Make sure you place the box in an
			easy to clean area!</p>
		<p>This can also be done with scraps of cloth or shredded paper,
			cardboard, tissies, paper towels, clean wood blocks, wine corks, or
			anything you know your rats would love to chew on and dig through.</p>
