		<h2>
			<a id="peaFishing"></a>Fishing for Peas
		</h2>
		<p>
			<a
				href="https://lh5.googleusercontent.com/-amdUBCx6s48/VIkcWXQto-I/AAAAA
		AAAAPw/CITgBhf0-NI/w640-h480-no/pea-fishing.jpg"><img
				src="https://lh5.goo
		gleusercontent.com/-amdUBCx6s48/VIkcWXQto-I/AAAAAAAAAPw/CITgBhf0-NI/w640-h
		480-no/pea-fishing.jpg"
				alt="pea fishing" title="Fishing for Peas" width="500"></a>
		</p>
		<p>This is a great way to cool down your rats on a hot summer day.</p>
		<p>Grab a shallow tray that your rats will fit in. Make sure the
			weight of your rat sitting on the edge will not tip it over. Fill
			shallowly with water. Toss in a few frozen peas. Place in a
			waterproof area, like the bathroom floor or a plastic tarp. Keep a
			towel handy, just in case one of your rats attempts to sneak off and
			make a mess. Rats will jump or wade in the water to nab the peas. If
			the rats are more nervous, they may fish for peas with their paws
			from the pool side.</p>
