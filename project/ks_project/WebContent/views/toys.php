			In my own opinion, since rats chew on everything (yes, <b>everything</b>)
			you might as well make the toys yourself for cheap. The rats won't
			care, and I know my rats have more fun shredding toys instead of
			playing with them. Here are some do-it-yourself rat entertainment
			options!
		</p>

