		<h2>
			<a id="beanBags"></a>Beanbags
		</h2>
		<p>
			<a
				href="https://lh3.googleusercontent.com/-LqRBdHYkxbQ/VIkn7K9-a1I/AAAAAAAAARo/CSK5VrqedgA/w340-h270-no/beanbag.jpg"><img
				src="https://lh3.googleusercontent.com/-LqRBdHYkxbQ/VIkn7K9-a1I/AAAAAAAAARo/CSK5VrqedgA/w340-h270-no/beanbag.jpg"
				alt="bean bag" title="Bean Bag" width="500"></a>
		</p>
		<p>Beanbags are a great way to help keep your rat cool in the summer
			and warm in the winter. A warm beanbag is also useful for sick rats
			to snuggle up to.</p>
		<p>To create a beanbag, cut up some tough fabric (like denim) into a
			square. Size depending on the number of rats. Sew up the side seems
			so that it forms a pillow case, leaving one side open. Fill loosely
			with whole uncooked wheat grains, rice, or small dried beans. Sew up
			the end to close it completely.</p>
		<p>
			Wheat grains, rice, or beans hold temperature very well. For hot
			summer days, place the beanbag in the fridge or freezer to cool down
			before giving it to your rats. In the winter, it can be microwaved
			until it's <b>warm,</b> not hot. Extra layers of fabric may be
			desired if your rats are shredders. However, if they do manage to
			chew into the beanbag, some dry wheat or rice won't hurt them.
		</p>
