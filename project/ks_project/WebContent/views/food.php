		<p>Rats will eat just about anything, whether it's healthy for them or
			not. There are many different ways to feed your rats, but having a
			good variety of fresh food and a dry mix is best. Personally, I feed
			my rats a mix of lab blocks (Oxbow Essentials Adult Rat Food) and a
			mixed food from the pet store, as well as other fresh suppliments. 
			When selecting a mix food, select one that has few peanuts, NO corn, 
			and sunflower seeds... they are high in fat and can lead to rat obesity.</p>
		<h2>Dry Mix</h2>
		<p>Many other rat owners put together their own mix of rat food. Here
			are some ideas to add into a dry mix:</p>
		<ul>
			<li>Rolled oats</li>
			<li>Low fat mixed grain cereals</li>
			<li>Lab blocks</li>
			<li>Puffed wheat cereal</li>
			<li>Puffed rice cereal</li>
			<li>Total cereal</li>
			<li>Dried fruits (make sure there is no added sugar!)</li>
			<li>Dry pasta</li>
			<li>Sunflower seeds (<i>very sparingly</i>, unstalted)</li>
		</ul>
		<p>
			When mixed together, store in an airtight container in a 
			cool, dry place.
		</p>
		<h2>Fresh Food</h2>
		<p>
			A good rat diet will consist of fresh foods as suppliments 
			to their dry mix. Keep in mind as rats are small animals, 
			a half-inch cube is the proper serving for several fruits. 
			Remove any fresh food that has not been eaten within 2-3 hours. 
			A list of fresh fruits and vegetables follows: 
		</p>
		<ul>
			<li>Apples</li>
			<li>Cherries (pitted)</li>
			<li>Grapes (seedless)</li>
			<li>Banana</li>
			<li>Berries</li>
			<li>Pears</li>
			<li>Melons</li>
			<li>Plums</li>
			<li>Broccoli</li>
			<li>Peas</li>
			<li>Carrot</li>
			<li>Collard greens</li>
			<li>Cucumber</li>
			<li>Alphalfa sprouts</li>
			<li>Cooked sweet potato</li>
			<li>Cauliflower</li>
			<li>Kale</li>
			<li>Celery</li>
			<li>Cooked corn on the cob</li>
			<li>Romaine lettuce</li>
			<li>Parsley</li>
			<li>Bok choy</li>
			<li>Squash</li>
		</ul>
		<h2>What Not to Feed</h2>
		<p>
			Avoid feeding any of the following to your pet rats:
		</p>
		<ul>
			<li>Chocolate</li>
			<li>Carbonated beverages</li>
			<li>Caffinated Beverages</li>
			<li>Cabbage</li>
			<li>Brussel Sprouts</li>
			<li>Raw sweet potato</li>
			<li>Too much cheese</li>
			<li>Dried corn</li>
			<li>Sweet sugar treats or junk food</li>
			<li>Raw beans</li>
			<li>Peanut butter (it's very sticky and they can choke on it)</li>
			<li>Green banana</li>
			<li>Citrus juice (this causes cancer in male rats, although 
			it protects against mammary tumors in females)</li>
		</ul>
		<h2>Treats</h2>
		<p>
			Treats should be given <b>sparingly</b>. Fresh fruits and vegetables are 
			better treats than the following, but I do give my rats a yogurt 
			drop when they have behaved exceptionally well.
		</p>
		<ul>
			<li>Plain popped popcorn</li>
			<li>Chicken or beef bones (cooked/boiled)</li>
			<li>Yogurt drops</li>
			<li>Nuts in their shell (macadamias, pecans, walnuts, hazelnuts...)</li>
			<li>Dog biscuits (low calorie, low fat)</li>
			<li>Rat seed sticks</li>
			<li>Gerber Graduates Puffs Cereal Snack</li>
			<li>Dried fruits</li>
			<li>Plain saltine crackers (no salt)</li>
			<li>Lean meats (chicken, turkey)</li>
			<li>Rice cakes</li>
			<li>Cheese</li>
			<li>Whole wheat pasta/bread</li>
			<li>Cooked beans</li>
			<li>Mealworms</li>
			<li>Yogurt (with live cultures)</li>
			<li>Brown rice</li>
			<li>Some meal leftovers are okay in moderation, but avoid ones 
			with lots of spices or if they are high in fat/sugar.</li>
		</ul>
		<p>
			When giving treats, remember moderation is key. Rats will always 
			attempt to get another treat by giving you the sad face. Don't 
			give in!
		</p>
