		<p>
			<b>Cages</b>. Wire cages can be found in a variety of sizes, designs, and costs. 
			The most important advantage to a wire cage is ventilation, as the open wires 
			keep ammonia fumes to a minimum. Cages also offer your rats more stimulation, 
			as it is easier to implement additional levels and ramps to move around on, as 
			well as places to hang accessories. They are also more open to interaction with 
			the rest of the household. However, there are some downsides to cages. Rat food 
			and litter are more likely to end up on the floor than other types of housing. 
			Wire floors can also lead to foot injuries or can aggrivate a condition known as 
			bumblefoot. However, covering wire floor with solid flooring such as linoleum, 
			cloth, or needlepoint mesh can alleviate this issue.
		</p>
		<p>
			<a href="https://lh5.googleusercontent.com/-Rz1P_rGzRo0/VIkQsRljgnI/AAAAAAAAAOw/
			AVbNq6Yl_Pc/w612-h816-no/cage-aquarium.jpg"><img src="https://
			lh5.googleusercontent.com/-Rz1P_rGzRo0/VIkQsRljgnI/AAAAAAAAAOw/AVbNq6Yl_Pc/w612-
			h816-no/cage-aquarium.jpg" alt="aquarium" width="500"></a>
		</p>
		<p>
			<b>Aquariums</b>. Glass aquariums typically come in a variety of sizes and 
			heights. If using just a glass aquarium, make sure that the cover locks down 
			to prevent rats from escaping. Special water bottles that hang from the edges 
			of the tank will need to be obtained. Many people find aquariums easier to clean, 
			and rat food and litter remains inside the aquarium instead of ending up on 
			the floor. Aquariums are also less drafty than cages, which is good for a hairless 
			rat or a rat that is ill. However, aquariums are more prone to elevated ammonia 
			fumes and overheating. They will need to be cleaned much more often for health 
			purposes. Glass is also easier to break than wire cages. However, as depected above, 
			expandable cage toppers that fit on top of aquariums are available to add space 
			and ventilation to a rat home.
		</p>
		<p>
