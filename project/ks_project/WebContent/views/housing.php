		<h1>Housing</h1>
		<p>
			There are lots of options for housing pet rats! Just keep in mind that there 
			needs to be enough space for all the rats housed. It is also important to 
			choose housing that is sturdy and escape-proof. Rats are very clever and if it's 
			possible to escape, they will! Do not use things like hampster cages (the bars are 
			too far apart and the rats will squeeze through) or plastic storage bins as 
			permanent housing.
		</p>
		<p>
			Cages will also need to be cleaned regularly. Surfaces will need to be wiped 
			down with a mild, safe cleaning solution and bedding will need to be changed 
			as needed. Sterilization of the cage weekly will help eliminate oder, 
			parasites, and bacteria.
		</p>
		<h2>Environment</h2>
		<p>
			The typical housing for rats are cages, aquariums, and homemade residences. 
			Each have pros and cons.
		</p>
		<p>
		<a href="https://lh3.googleusercontent.com/-ZhE5d1KNjCo/VIkKIW48k_I/AAAAAAAAAOc
