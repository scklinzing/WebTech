		</p>
		<p>
			<b>Homemade Cabinet Housing</b>. Another housing option are homemade cabinet 
			style cages, sometimes called "grotto" cages. One of the best things about 
			cabinet cages is that the rat room generally remains much cleaner than with 
			the conventional cage system. Also, the many levels leaves ample room for 
			placing toys, hammocks, and other things to keep your rats stimulated. 
			However, one of the cons of cabinet cages is that they are usually larger and 
			thus harder to move around and sterilize. One important thing is that the 
			wood needs to be protected. Bare wood can soak up urine and become very unpleasant 
			for you, and unhealthy for your rats.
		</p>
